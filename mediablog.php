45.  The first time, he plays only a few screeching notes before Robert sets it down.
46.  He continues to play as he works with Alexia.
The Duet
47.  One day, he comes into a practice with Alexia only to find that Alexia has started playing without him. She’s playing Nick’s part in the duet that he wrote for them.
48.  Robert flips out and scares Alexia off.
49.  That night at home, Robert sits down and plays a portion of his own part of the duet and comes to the realization that he wants to play the piece.
50.  The next practice, he asks Alexia if she would play the duet with him to kick off her recital at the end of the semester.
Back in the Game
51.  After 23 years of marriage, Robert finds himself back in the game, ironically courting his wife. They do things like going out to dinner and seeing movies.
52.  They seem to be falling back in love. Slowly, a few of Michelle’s things find their way back into the house.
53.  Robert stops taking his antidepressants, happy with his life for the first time in five years.
54.  Neither of them acknowledges their history. Neither of them ever brings up Nick.
55.  Michelle and Robert decide to attend/host the anniversary party together as a couple once more.
A Concert to Prepare for
56.  Robert and Alexia prepare for the concert. Their lessons turn from educational to practicing for the recital—practicing the duet.
57.  They are meshing well and playing well together. They are able to laugh and be free with one another.
58.  Alexia allows Robert to teach her some new tricks.
59.  Robert also allows Alexia to teach him some different methods and to step a little outside his comfort zone.
60.  Robert invites Alexia to the party, as he has already started to consider her a substitute child. Alexia happily accepts the invitation, not precisely knowing what she was getting herself into.
An Anniversary to Remember
61.  Robert shows up at the party with both Michelle and Alexia on his arms, which creates a bit of a stir.
62.  Cooper gives him grief about Alexia again and inquires if there is anything romantic going on, which Robert vehemently denies. He informs Cooper that if anything he sees her as a daughter, not a lover.
63.  Though Robert and Michelle look happy together, it is obvious to many of the people in attendance, Alexia included, that they don’t love each other and are only together out of denial over what had happened.
64.  Alexia catches herself staring at Robert with less-than-appropriate thoughts going through her mind. Cooper catches her watching Robert and confesses his own misgivings about Robert’s marriage, which only serves to spur Alexia’s feelings.
The “Perfect” Family
65.  Cooper and his wife GWEN KAPLAN, 40s, share their party with their two lovely children, who are both around Alexia’s age and seem to be the picture perfect image of what a family is supposed to be.
66.  Michelle and Robert barely conceal their jealousy over the wonderful lives their friends lead, and it only reminds them of how truly messed up their own situation is.
The Wrong Name
67.  While Alexia is in bed with her boyfriend, she utters Robert’s name.
68.  Her boyfriend gets upset, assuming that she’s cheating on him, and breaks up with her.
The Good Son
69.  Robert goes into Nick’s room for the first time in a long time. He settles down on the bed, but something crinkles under the pillow. Robert pulls out two pieces of paper from under the pillow. One is an acceptance letter to Juilliard, the other an acceptance letter to UCLA.
70.  Robert remembers back to an argument he had with Nick, not long before the accident about where he was going to school. Nick wanted to give up on his music to pursue marine biology, but Robert refused, claiming that it would be a waste of Nick’s talent.
Lament and Guilt
71.  Alexia mentions to Robert that she thought he seemed sort of sad at the party.
72.  Robert tells Alexia about what happened with Nick, attempting to convey how much he misses his son.
73.  Alexia seems guilty about something but doesn’t say anything about it.
Sealed with a Kiss
74.  That night, Alexia writes a letter in her dorm, addressed to her parents. One can infer from the letter that she ran away from home a number of years ago and is not an orphan.
75.  Alexia asks her friend EMILY COOKE, 19, sweet and outspoken, to send the letter after Alexia has left for Thanksgiving.
76.  Not realizing what it was, Emily sends it from New York, not thinking twice about it.
Being Realistic
77.  Cooper and Robert have another one of their chats, and Cooper confronts Robert about being realistic in his expectations about his relationship with Michelle.
78.  Robert writes off Cooper when he tells Robert of Alexia’s feelings for him.
Can’t Stop the Music
79.  Robert surprises Michelle by taking her to a classical concert thinking that she will
enjoy it because she used to love classical music.
80.  Michelle can’t handle the classical music after what happened to Nick and storms out, saying that she doesn’t know if she can keep doing this.
Foul Moods
81.  Robert arrives at the next rehearsal in a foul mood over what is going on with Michelle, but Alexia doesn’t want to take his crap.
82.  Robert keeps treating her like a child—like his child—and she snaps at him, exclaiming that she is not his daughter and she doesn’t appreciate being treated as though she is.
83.  Robert tries to explain that, since she’s even younger than his son, it just sort of feels right to have her fill in that space. She gets frustrated and runs out on him.
Plea Bargain
84.  Even worse off than before, Robert ends up going to a bar and getting fairly drunk before returning home late in the evening, only to find Michelle waiting for him.
85.  She explains that she can’t be with Robert. There’s just too much history and too many bad memories that she can’t deal with. Robert accuses her of trying to erase her life with him, and she doesn’t deny it.
86.  She practically begs him to just sign the divorce papers so they can both move on with their lives.
Dissonance Redux
87.  More upset than before, Robert shows up to rehearsal in a state that is less than sober and seems intent on ruining whatever semblance of happiness he had been able to accrue over the past few months.
88.  He verbally attacks Alexia, calling her ungrateful, as he wanted to give her a family and thought she would understand that.

89.  Alexia almost slips and tells him the truth but is upset enough over his accusations that she ends up leaving before she ruins everything.
An Unexpected Meeting
90.  After storming out on Robert for the second time, Alexia makes her way to the subway to get away from school for a little bit. However, she quickly encounters another problem: her mother is on the train with her.
91.  Alexia tries to ignore her mother’s attempts to get her attention, but the woman makes her way over to Alexia.
Gone
92.  Robert starts to slowly lose it as he beings to realize that Alexia has gone missing a week before the concert.
Alternative Solutions
93.  Jonathan tells Robert to begin practicing the duet with another of his students, since the performance has already been heavily advertised and the tickets have sold out.
94.  Robert works with the other student, but he is a very classical performer and isn’t able to convey the same energy that Alexia did.
So Perfect
95.  Robert complains to Cooper about Alexia’s absence and Michelle’s adamant refusal to see or talk to him until the divorce papers are signed.
96.  Cooper mentions something about how other people have problems too, and Robert nastily strikes back at him about how Cooper has the perfect family and shouldn’t complain.
97.  They get in a fight, and Robert leaves.
A Familial Truth
98.  The night of the concert, Alexia shows up backstage and accidentally lets slip that the reason for her absence was that she ran into her mother.
99.  A big fight breaks out as Robert realizes that Alexia has been lying to him the whole time and is not, in fact, an orphan at all.
100. Alexia comes clean about her past.
The Last Goodbye
101. After they both calm down a little bit, Robert apologizes for the things he said to her before she left and explained that it was because he was coming to terms with the fact that his marriage was over.
102. Alexia confesses her feelings for Robert by kissing him, and Robert explains to her that he had meant what he said about her being like a daughter.
103. Overcoming her embarrassment, she chooses to go on stage with him.
Something to Remember Me By
104. Robert seemed mostly okay before stepping onstage. However, once under the lights, Robert starts getting incredibly anxious.
105. He sits down before they begin playing, but flashes of the night Nick keep coming to him, and before he knows it he’s stuck in the nightmare of holding onto his dead son’s bleeding body.
106. Robert snaps out of it to find himself on the floor in tears and can’t continue with the performance.
Coming Clean
107. Robert puts in his resignation,and Jonathan comes clean about the performance clause not existing.
108. Cooper reconciles with Robert, making sure that he’s leaving for the right reasons.
Moving On
109. Robert moves out of his house, boxing up Nick’s possessions, taking only his acceptance letters and his cello.
110. He moves into a modest apartment in New York City.
111. Robert ends up signing the divorce papers and receives a thank you note from Michelle.
112. He begins teaching lessons again, but privately.
Park Performance
113. In the spring, Robert goes out to a park and plays some music. When he gets to the duet, he starts playing his part, his eyes falling closed as he does so.
114. Partway through, another cello jumps in, playing the other part.
115.  Surprised, Robert opens his eyes to find Alexia is playing with him.
116. They smile at each other and nod in understanding and forgiveness.


