13.  Robert storms into Jonathan’s office asking why Alexia was sent to him for lessons.
14.  Jonathan explains that Alexia is a unique case, and he believes that Robert would work well with her because there was something about her that reminded him of Nick.
15.  Jonathan asks nicely if Robert can find a way to fit Alexia into his lesson schedule, and Robert obliges him.
Dissonance
16.  Robert and Alexia have their first lesson. They learn that they clash both in personality and style.
17.  Alexia is set in her ways and doesn’t seem to want to learn the proper way of doing things.
18.  Robert is also set in his ways and refuses to budge on his traditional sense of teaching and playing.
19.  Alexia challenges Robert to show her how it’s done if his way is so much better than hers, but Robert refuses. Alexia calls him out on the fact that he’s a music teacher, but he won’t play. Claims that he must not have been very good.
20.  She pushes enough buttons that eventually Robert blows up and they get in a big fight.
An Ultimatum
21.  Yet again, Robert finds himself in Jonathan’s office, though this time it is with conviction that Robert refuses to work with Alexia.
22.  Jonathan presents him with an ultimatum: either work with Alexia or don’t work at Juilliard. Robert questions Jonathan on what grounds he would be fired, and Jonathan unsympathetically pulls out a performance clause in Robert’s contract.
Choices, Choices
23.  Robert pays a visit to his best friend, COOPER KAPLAN, 40s, devilishly handsome, in search of advice on what to do.
24.  Cooper tells him to train the girl and that it isn’t worth losing a job over. He also asks if there was any attraction.
25.  Robert inquires why Cooper never comes to see him anymore. After getting sufficiently awkward, Cooper admits that he’s been waiting for Robert to come to him.
26.  Cooper mentions the anniversary party that Michelle is throwing for Cooper and his wife.
Second Chances
27.  Robert has decided to go through the lessons with Alexia, and while she is no less surly this time around, they both have resigned themselves to their fates.
28.  Robert decides to check to see where Alexia is at musically and is both stunned and appalled to find how well she plays for how little she seems to know about music.
29.  Alexia snaps moodily about how she doesn’t need to read music, her music comes from within.
Cordially Invited
30.  Arriving home from a long day of lessons, Robert finds that he has received an official invitation to the party for Cooper and is surprised to see that it was addressed by Michelle.
Ex Marks the Spot
31.  Robert shows up at Michelle’s art gallery requesting an audience with her. The receptionist is snippy with him, but he eventually gets in.
32.  Robert wonders what Michelle meant by personally sending him an invitation, but Michelle warns him to not read into it.
33.  When it seems that Robert is just using the invitation as an opportunity to gauge Michelle’s feelings toward him and the idea of reconciling, Michelle blatantly refuses him, telling him to move on.
Third Time’s a Charm?
34.  The lessons don’t seem to be getting any better as Alexia’s New York City attitude gets in the way of her showing any respect for Robert or his teaching methods.
35.  Robert refuses to show her respect because of it, resulting in a fight that breaks out over bowing positions.
Playing Nice
36.  Worn out, Robert returns home to find Michelle sitting on the front stoop.
37.  Michelle explains that she would like to reconsider giving their marriage a second try, though doesn’t really explain why.
38.  Robert invites her in and they share a slightly awkward, but nice, dinner.
39.  At the end of the evening, neither of them seem sure of what should happen. Michelle eventually leaves for her New York apartment, despite Robert’s invitation to stay the night.
Similar Heritage
40.  Robert seems happier at the next lesson, but his mood is fouled by Alexia’s less than enthusiastic attitude.

